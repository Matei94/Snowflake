int norminf(int i, int j) {
  if (i<0) i = -i;
  if (j<0) j = -j;

  if (i > j) {
    return i;
  } else {
    return j;
  }
}

int seminorm(int i, int j) {
  int k;

  k = i+j;
  if (k>=0) {
    return k;
  } else {
    return -k;
  }
}